    private void btnModificarDirectorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarDirectorActionPerformed
        Director directorModificado = null;
        if(txtNuevoNombre.getText().isEmpty() || txtNuevoApellido.getText().isEmpty()){
                lblOculto.setText("El nombre y apellidos no pueden estar vac√≠os");
                lblOculto.setVisible(true);
        }else{
            directorModificado = crearDirectorModificado();
           
            try {GestionDao gDao = new GestionDao();
                VentanaPrincipal vp = new VentanaPrincipal();
                DirectorDAO dDao = new DirectorDAO();
                boolean existe = dDao.existeDirector(directorModificado);
                if(existe){
                    vp.crearAviso("Director existente.");
                }else{

                    try { String update = "UPDATE director SET nombre = ?, apellidos = ? where apellidos = ?;";
                        gDao.conectar();
                        PreparedStatement ps = GestionDao.conexion.prepareStatement(update);
                        ps.setString(1, directorModificado.getNombre());
                        ps.setString(2, directorModificado.getApellidos());
                        ps.setString(3, apellidos);
                        ps.executeUpdate();
                        ps.close();
                        gDao.desconectar();
                        vp.crearAviso("Director modificado.");
                    } catch (SQLException ex) {
                        Logger.getLogger(VentanaModificarDirector.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                dispose();
            } catch (SQLException ex) {
                Logger.getLogger(VentanaModificarDirector.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
               
    }//GEN-LAST:event_btnModificarDirectorActionPerformed

